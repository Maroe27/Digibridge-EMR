    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
    FileNameExtensionFilter filter = new FileNameExtensionFilter("PDF Files only", "pdf");
    FileChooser.setFileFilter(filter);

    // Show the file chooser dialog
    int result = FileChooser.showOpenDialog(this);
    DefaultTableModel tableModel = new DefaultTableModel();
    
    // Check if a file was selected
    if (result == JFileChooser.APPROVE_OPTION) {
    try {
        // Get the selected file
        File selectedFile = FileChooser.getSelectedFile();

        // Load the PDF document
        PDDocument document = PDDocument.load(selectedFile);
        PDFRenderer renderer = new PDFRenderer(document);

        // Render each page of the PDF document to images
        int numPages = document.getNumberOfPages();
        BufferedImage[] pageImages = new BufferedImage[numPages];
        for (int i = 0; i < numPages; i++) {
            pageImages[i] = renderer.renderImageWithDPI(i, 100);
        }
        document.close();

        // Combine all page images into one image
        int width = 0;
        int height = 0;
        for (BufferedImage img : pageImages) {
            width = Math.max(width, img.getWidth());
            height += img.getHeight();
        }
        BufferedImage combinedImage = new BufferedImage(width, height, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2d = combinedImage.createGraphics();
        int y = 0;
        for (BufferedImage img : pageImages) {
            g2d.drawImage(img, 0, y, null);
            y += img.getHeight();
        }
        g2d.dispose();

        // Update the pdfPanel with the new PDF content
        ((PDFPanel)jPanel18).setCombinedImage(combinedImage);
        ((PDFPanel)jPanel18).setPreferredSize(new Dimension(width, height));
        jPanel18.repaint();
    } catch (IOException ex) {
        Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
    }
}

    }//GEN-LAST:event_jButton3ActionPerformed

