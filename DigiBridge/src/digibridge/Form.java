    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        // Get email and password inputs
        String email = emailcreate.getText();
        String password = passcreate.getText();
        
        // Flag variables for email and password validation
        boolean isEmailValid = false;
        boolean isPasswordValid = false;

        // Regular expression pattern for email validation
        String EMAIL_PATTERN = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";

        // Compile the pattern
        Pattern emailPattern = Pattern.compile(EMAIL_PATTERN);

        // Matcher for email validation
        Matcher emailMatcher = emailPattern.matcher(email);

        // Check if email is valid
        if (emailMatcher.matches() && method.getEmailCount(email) == 0){ 
            isEmailValid = true;
        } 
        // Check if email is already registered
        else if(method.getEmailCount(email) >= 1) {
            JOptionPane.showMessageDialog(null, "Email/username is already taken");
        } else {
            JOptionPane.showMessageDialog(null, "Email is not valid");
        }

        // Password validation
        if (password.matches(".*[a-z].*") && password.matches(".*[A-Z].*") && password.matches(".*\\d.*") && password.matches(".*[!@#$%^&*()\\-_=+\\\\|\\[{\\]};:'\",<.>/?`~].*") && password.length() >= 8) {
            isPasswordValid = true;
        } else {
            JOptionPane.showMessageDialog(null, "Password must have a minimum of 8 characters, a capital letter, small letter, number, and special character");
            // Change the color of labels to indicate invalid password
            jLabel21.setForeground(Color.red);
            jLabel31.setForeground(Color.red);
        }
        
        //Store to DB if both are valid
        if(isEmailValid && isPasswordValid) {
            method.storeUser(email, password);
            //Send request through email
            methods.sendAccessRequest(AdminEmail,email);
            JOptionPane.showMessageDialog(null, "Account Request Sent!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

